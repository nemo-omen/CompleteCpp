#include<iostream>
using namespace std;

int main()
{
  int a = 10;

  int b = 3;

  int sum = a + b;

  int difference = a - b;

  int product = a * b;

  int quotient = a / b;

  int remainder = a % b;

  cout <<"sum = "<< sum << endl;
  cout <<"difference = "<< difference << endl;
  cout <<"product = "<< product << endl;
  cout <<"quotient = "<< quotient << endl;
  cout <<"remainder = "<< remainder << endl;
  return 0;
}